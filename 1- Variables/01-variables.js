//What is a variable?

//A container that stores some value. We use: let → value can change, const → value cannot change//

const myName = "Saba";
let age = 27;
let city = "Delhi";

console.log("Name:", myName);
console.log("Age:", age);
console.log("City:", city);

// Try changing age
age = 28;
console.log("Updated age:", age);

