//for loop

for (let i = 1; i <= 5; i++) {
  console.log("Number:", i);
}

//while loop

let count = 1;

while (count <= 3) {
  console.log("Count:", count);
  count++;
}

//Loop through an ARRAY

let colors = ["red", "blue", "green"];

for (let i = 0; i < colors.length; i++) {
  console.log(colors[i]);
}


