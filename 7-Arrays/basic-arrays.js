//Create an Array
let fruits = ["apple", "banana", "mango"];
console.log(fruits);

//Accessing Array Elements
console.log(fruits[0]); // apple
console.log(fruits[1]); // banana

//Adding Elements
fruits.push("orange");  
console.log(fruits);

//Removing Elements
fruits.pop();
console.log(fruits);

//Array Length
console.log(fruits.length);
